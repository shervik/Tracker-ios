    static var ypRed: UIColor { UIColor(named: "Red") ?? UIColor() }
    static var ypLightGray: UIColor { UIColor(named: "Light_gray") ?? UIColor() }
    static var ypWhite: UIColor { UIColor(named: "White") ?? UIColor() }
}
